package dataTypes;

public class JavaOperators {

	public static void main(String[] args) {

		/**  There are 5 types of Java Operators
		 * 
		 * Arithmetic Operators 
		 * Assignment Operators
		 * Comparison/Relational Operators 
		 * Logical Operators
		 * Bitwise Operators
		 * 
		 */
		
		
		//learning Arithmetic Operators 
		// They are + - */ % ++ - -
		 
		
		//declaring variables
		
		int a = 10;
		int b = 5;
		int result;
		
		result = a + b; 
		System.out.println("Addition result is: " + result);
		result = a - b;
		System.out.println("Subtraction result is: " + result);

		result = a * b; 
		System.out.println("Multiplacation result is: " + result);

		result = a / b; 
		System.out.println("Division result is: " + result);

		result = a % b; 
		System.out.println("modulus result is: " + result);
		
		result = a++;
		System.out.println("incremental result is: " + result);
		
		result = a--;
		System.out.println("Decremental result is: " + result);
		
		

		


		
	}

}
